    private void CmdCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CmdCalcActionPerformed
        //----- VARIABLES -----//
        String nombre="";
        String puesto="";
        double sueldo=0.0;
        //------------------------//
        nombre = TxtNombre.getText();
        puesto = CmbPuesto.getSelectedItem().toString();
        switch(puesto)
        {
            case "Contador":
                sueldo=6000;
                break;
            case "Auxiliar":
                sueldo=2000;
                break;
            case "Secretaria":
                sueldo=2500;
                break;
            case "Vendedora":
                sueldo=1000;
                break;
            case "Chofer":
                sueldo=1800;
                break;
            case "Mensajero":
                sueldo=900;
                break;
        }
        //--------------------------//
        double sueldoExtra=0, comisiones=0, otrosIngresos=0;
        sueldoExtra=Double.parseDouble(TxtSEx.getText());
        comisiones=Double.parseDouble(TxtCm.getText());
        otrosIngresos=Double.parseDouble(TxtOI.getText());
        //----------------------------------------------//
        double totIngresos=0;
        //--- SE ADICIONAN 154.5, VALOR DE LA BONIFICACIÓN INCENTIVO
        totIngresos = 154.5+sueldo+sueldoExtra+comisiones+otrosIngresos;
        //-------------------------------------------------------//
        double igss=0;
        igss=sueldo*0.0483;
        double isr=0;
        if(sueldo>=5000){
            isr=71.98;
        }
        double anticipos=0, dj=0, otrosDescuentos=0;
        double totDesc=0;
        anticipos=Double.parseDouble(TxtAnt.getText());
        dj=Double.parseDouble(TxtDJ.getText());
        otrosDescuentos=Double.parseDouble(TxtOD.getText());
        //---------
        totDesc=igss+isr+anticipos+dj+otrosDescuentos;
        //---------------------//
        double sueldoLiquido=0;
        sueldoLiquido = totIngresos-totDesc;
        //---------------------------------------//
        Object[] data = new Object[15];
        data[0]=nombre;
        data[1]=puesto;
        data[2]=String.valueOf(sueldo);
        data[3]=String.valueOf(sueldoExtra);
        data[4]="154.5";
        data[5]=String.valueOf(comisiones);
        data[6]=String.valueOf(otrosIngresos);
        data[7]=String.valueOf(totIngresos);
        data[8]=String.valueOf(igss);
        data[9]="71.98";
        data[10]=String.valueOf(anticipos);
        data[11]=String.valueOf(dj);
        data[12]=String.valueOf(otrosDescuentos);
        data[13]=String.valueOf(totDesc);
        data[14]=String.valueOf(sueldoLiquido);
        DefaultTableModel modelo = (DefaultTableModel) TbData.getModel();
        modelo.addRow(data);
        TbData.setModel(modelo);
        //--------------------------//
        TxtNombre.setText("");
        TxtSEx.setText("");
        TxtCm.setText("");
        TxtOI.setText("");
        TxtAnt.setText("");
        TxtOD.setText("");
        TxtDJ.setText("");
        TxtNombre.requestFocus();
    }//GEN-LAST:event_CmdCalcActionPerformed

