    private void Buscar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Buscar2ActionPerformed
        //Codigo que sirve para buscar a la persona en la base de datos
        // Codigo para buscar datos en la base de datos:
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/Proyectofinal", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from concepto where id_empleado = ?");
            pst.setString(1, txtBuscar.getText().trim());
            ResultSet rs = pst.executeQuery();

            if(rs.next()){

                txtid1.setText(rs.getString("id_empleado"));
                txtnombres1.setText(rs.getString("nombre_empleado"));
                aviso.setText(rs.getString("estatus"));
                txtnaturaleza.setText(rs.getString("naturaleza"));
                txttipo.setText(rs.getString("tipo"));
                txtsueldo2.setText(rs.getString("sueldo_ordinario"));
                txtsueldoextra2.setText(rs.getString("sueldo_extraordinario"));
                txtcomis2.setText(rs.getString("comisiones"));
                txtboni.setText(rs.getString("bonificaciones"));
                txtotross2.setText(rs.getString("otros_ingresos"));
                txtanticipos.setText(rs.getString("anticipos"));
                txtjudicial.setText(rs.getString("descuento_judicial"));
                txtotrosss.setText(rs.getString("otros_descuentos"));
                txtinicial.setText(rs.getString("fecha_inicial"));
                txtfinal.setText(rs.getString("fecha_final"));

            } else {
                JOptionPane.showMessageDialog(null, "Persona no registrada.");
            }

        }catch (Exception e){

        }
    }//GEN-LAST:event_Buscar2ActionPerformed

    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed

        //Codigo echo por Jaqueline Carrera
        //Codigo que ingresa los datos a la tabla con sus respectivas condiciones
        //Se empiezan a poner las variables para los txtfield
        String Id=txtid1.getText(),Nombre=txtnombres1.getText(),Natu=txtnaturaleza.getText();
        String Tip=txttipo.getText();
        String fechai=txtinicial.getText();
        String fechaf=txtfinal.getText();
        String selec;
        selec = aviso.getText();

        float SueldoO=Float.parseFloat(txtsueldo2.getText());
        float SueldoE=Float.parseFloat(txtsueldoextra2.getText());
        float comisiones=Float.parseFloat(txtcomis2.getText());
        float bonificaciones=Float.parseFloat(txtcomis2.getText());
        float Otrosi=Float.parseFloat(txtotross2.getText());
        float TotalIngresos;
        float anticipos=Float.parseFloat(txtanticipos.getText());
        float Desc=Float.parseFloat(txtjudicial.getText());
        float Otrosd=Float.parseFloat(txtotrosss.getText());
        float igss1;
        float isr1 = 0;
        float TotalEgresos;
        float SueldoLiquido;
        //Condicion del ISR para sueldos mayores de 5000
        if(SueldoO>=5000){
            isr1=(float) (SueldoO*0.0145);
        }
        //Calculos del IGSS, Total ingresos, Total Egresos y Sueldo Liquido
        igss1= (float) (SueldoO*0.0483);
        TotalIngresos=(SueldoO+SueldoE+comisiones+Otrosi);
        TotalEgresos=(anticipos+Desc+Otrosd+igss1+isr1);
        SueldoLiquido=(TotalIngresos-TotalEgresos);
        //Datos que se iran a la tabla
        DefaultTableModel modelo=(DefaultTableModel)
        tabla.getModel();
        Object [] fila=new  Object[20];
        fila[0]=Id;
        fila[1]=Nombre;
        fila[2]=selec;
        fila[3]=Natu;
        fila[4]=Tip;
        fila[5]=SueldoO;
        fila[6]=SueldoE;
        fila[7]=comisiones;
        fila[8]=bonificaciones;
        fila[9]=Otrosi;
        fila[10]=TotalIngresos;
        fila[11]=anticipos;
        fila[12]=Desc;
        fila[13]=igss1;
        fila[14]=isr1;
        fila[15]=Otrosd;
        fila[16]=TotalEgresos;
        fila[17]=fechai;
        fila[18]=fechaf;
        fila[19]=SueldoLiquido;
        modelo.addRow(fila);
        tabla.setModel(modelo);
    }//GEN-LAST:event_jButton16ActionPerformed

